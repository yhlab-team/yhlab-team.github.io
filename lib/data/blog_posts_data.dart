## Nemo: 당신의 똑똑한 학습 파트너

바로 이 지점에서 Nemo가 시작됩니다.

Nemo는 망각곡선 이론을 기반으로 각 학습 카드의 복습 주기를 자동으로 계산합니다. 당신이 해야 할 일은 단 하나, Nemo가 알려주는 대로 복습하는 것뿐입니다.

### Nemo는 어떻게 작동할까요?

1. 과학적 복습 간격 적용
Nemo는 망각곡선 연구에서 검증된 최적의 복습 주기를 자동으로 적용합니다. 1일 → 3일 → 1주일 → 2주일 → 1개월로 점진적으로 간격을 늘려가며 장기 기억을 형성합니다.

2. 자동 복습 알림
복습이 필요한 시점이 되면 Nemo가 알려줍니다. 언제 무엇을 복습해야 할지 고민할 필요가 없습니다. Nemo가 알려주는 대로만 따라가면 됩니다.

3. 효율적인 시간 관리
오늘 복습이 필요한 카드만 선별해서 보여주기 때문에, 불필요한 시간 낭비 없이 효율적으로 공부할 수 있습니다. 이미 외운 것을 반복하거나, 너무 늦게 복습하는 일이 없습니다.

4. 장기 기억 형성
과학적으로 검증된 간격 반복 학습을 통해 단기 기억을 자연스럽게 장기 기억으로 전환시킵니다. "잊어버리기 직전"의 최적 타이밍에 복습하여 기억을 강화합니다.

