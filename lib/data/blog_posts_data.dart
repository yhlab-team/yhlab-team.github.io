Nemo는 망각곡선 이론을 기반으로 각 학습 카드의 복습 주기를 자동으로 계산합니다. 당신이 해야 할 일은 단 하나, Nemo가 알려주는 대로 복습하는 것뿐입니다.

### Nemo는 어떻게 작동할까요?

1. **개인화된 학습 분석**: Nemo는 당신이 각 카드에 답한 결과를 분석합니다. 쉽게 맞춘 카드는 간격을 늘리고, 어려워한 카드는 더 자주 보여줍니다.

2. **자동 복습 스케줄**: 처음에는 짧은 간격(10분, 1시간, 1일)으로 복습을 권장하고, 정답률이 높아질수록 간격을 점진적으로 늘려갑니다(3일, 1주일, 2주일, 1개월...).

3. **효율적인 시간 관리**: 복습이 필요한 카드만 선별해서 보여주기 때문에, 불필요한 시간 낭비 없이 효율적으로 공부할 수 있습니다.

4. **장기 기억 형성**: 이렇게 개인화된 복습 스케줄을 통해 단기 기억을 자연스럽게 장기 기억으로 전환시킵니다.

