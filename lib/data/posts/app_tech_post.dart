- 새 내용 학습: 집중이 필요하므로 광고 OFF
- 복습: 상대적으로 쉬우므로 광고 ON

**전략 3: 학습 목표와 연계**
- 일일 학습 목표를 설정하고 꾸준히 실천
- 자연스럽게 포인트도 함께 적립

