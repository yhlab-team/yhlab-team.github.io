## PART 2. Nemo의 독특한 접근: 학습과 수익의 결합

### 완벽한 타이밍의 광고

Nemo는 다른 앱테크 앱과 완전히 다른 방식을 제공합니다.

**핵심 아이디어: "복습 대기 시간을 활용하자"**

간격 반복 학습의 특성상, 한 카드를 복습한 후 다음 카드로 넘어가기 전에 자연스러운 "대기 시간"이 존재합니다. 이 짧은 순간에 광고를 배치함으로써:

1. **학습 흐름을 해치지 않음**: 오히려 잠깐의 휴식 효과
2. **시간 낭비가 아님**: 어차피 필요한 휴식 시간 활용
3. **양심의 가책 제로**: 공부하면서 자연스럽게 수익 창출

### 어떻게 작동하나요?

**일반 학습 모드**
1. 영어 단어 카드 복습
2. "알았어요" 또는 "기억 안 나요" 선택
3. 다음 카드로 즉시 이동

**포인트 적립 모드**
1. 영어 단어 카드 복습
2. "알았어요" 또는 "기억 안 나요" 선택
3. **짧은 광고 시청 (15~30초)**
4. **포인트 적립!**
5. 다음 카드로 이동

광고 시청 여부는 선택 사항입니다. 시간이 없을 때는 건너뛰고, 여유가 있을 때는 포인트를 적립하면 됩니다.

## PART 3. 앱테크 vs 학습 효율: 균형 잡기

### 광고가 학습을 방해하지 않을까?

이건 중요한 질문입니다. 실제로 많은 사람들이 걱정하는 부분이죠.

**Nemo의 설계 철학:**

**1. 선택권은 사용자에게**
- 광고를 볼지 말지는 매번 선택 가능
- 시험 전날처럼 집중이 필요한 날: 광고 스킵
- 여유로운 날: 포인트 적립

**2. 짧고 명확한 광고**
- 15~30초의 짧은 광고만 선별
- 광고 후 바로 학습 재개
- 복잡한 상호작용 없음

**3. 자연스러운 휴식 효과**
- 20~30개 카드를 연속으로 보면 집중력 저하
- 광고가 오히려 짧은 휴식으로 작용
- 뇌에 잠깐의 휴식 시간 제공

### 과학적 근거: 마이크로 브레이크

최근 연구에 따르면 "마이크로 브레이크(Micro-break)"가 학습 효율을 높인다고 합니다:

- **연속 학습**: 30분 집중 → 피로도 증가 → 효율 저하
- **마이크로 브레이크**: 5분 학습 → 30초 휴식 → 5분 학습 → 집중력 유지

Nemo의 광고는 이 마이크로 브레이크 역할을 합니다. 단순히 포인트만 적립하는 게 아니라, 학습 효율까지 높이는 효과가 있습니다.

## PART 4. 실제 사용자 시나리오

### Case 1: 대학생 A씨의 하루

**오전 통학 시간 (30분)**
- Nemo로 영어 단어 30개 복습
- 광고 시청 모드 ON
- 학습 효과: 30개 단어 장기 기억 강화 + 포인트 적립

**점심시간 (10분)**
- 어제 배운 역사 용어 10개 복습
- 시간이 촉박해서 광고 OFF
- 빠르게 복습 완료

**저녁 퇴근길 (30분)**
- 자격증 공부 내용 20개 복습
- 광고 시청 모드 ON
- 학습 + 포인트 적립 동시 진행

**일상 속에서 자연스럽게**
학습 효과는 그대로 유지하면서 추가 혜택까지 받는 똑똑한 학습법

### Case 2: 직장인 B씨의 주말

**토요일 오전 - 집중 학습 모드**
