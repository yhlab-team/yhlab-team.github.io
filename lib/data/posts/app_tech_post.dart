- 순수하게 학습에만 집중

**토요일 오후 - 여유로운 복습**
- 이번 주에 배운 내용 전체 복습
- 광고 시청 모드 ON
- 소파에 편하게 앉아서 진행
- 학습하면서 자연스럽게 포인트도 적립

**전략적 활용**: 바쁠 때는 빠르게, 여유 있을 때는 혜택까지!

## PART 5. 다른 앱테크 앱과의 비교

### 일반 앱테크 앱

**장점:**
- 다양한 적립 방법
- 상대적으로 높은 수익률

**단점:**
- 순수한 시간 소비
- 다른 일과 병행 불가
- 광고만 보다가 시간 낭비했다는 죄책감

### Nemo 앱테크

**장점:**
- **공부하면서 자연스럽게 수익 창출**
- **추가 시간 투자 제로**
- **학습 효율 유지 또는 향상**
- 죄책감 없음 (오히려 뿌듯함)

**단점:**
- 순수 앱테크 앱보다는 포인트 적립 속도가 느림
- 학습 의지가 없으면 사용 불가

### 어떤 사람에게 적합할까?

Nemo 앱테크가 딱 맞는 사람:
- 영어, 자격증 등 암기 학습을 하는 사람
- 시간이 항상 부족한 학생/직장인
- 작은 혜택이라도 낭비하기 싫은 알뜰족
- 학습과 혜택, 두 마리 토끼를 잡고 싶은 사람

