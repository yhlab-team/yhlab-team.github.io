  fullContent: '''## PART 1. 앱테크와 공부, 동시에 가능할까?

### 바쁜 현대인의 고민

"영어 단어 외워야 하는데... 앱테크로 용돈도 벌고 싶고..."

학생이든 직장인이든, 시간은 항상 부족합니다. 공부도 해야 하고, 조금이라도 용돈을 벌고 싶은 마음도 있죠. 하지만 하루 24시간은 한정되어 있고, 둘 중 하나를 선택해야 하는 것처럼 느껴집니다.

